package features;

/**
 * @author ishaqkhan
 *
 */
public class Widget {

	String color;
	int weight;
	
	public Widget(String color, int weight) {
		super();
		this.color = color;
		this.weight = weight;
	}

	public Widget() {
		super();
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}
	
	
}
